class Car:
    def __init__(self, mark, motor, horsestrenght, fuel):
        self.mark = mark
        self.motor = motor
        self.horsestrenght = horsestrenght
        self.fuel = fuel
        print('Mark is: ', self.mark)
        print('Motor is: ', self.motor)
        print('Horsestrenght is: ', self.horsestrenght)
        print('Fuel is: ', self.fuel)

